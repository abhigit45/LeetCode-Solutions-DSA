# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def largestValues(self, root: Optional[TreeNode]) -> List[int]:
        que = deque()
        if not root:
            return []
        que.append(root)
        res = []
        
        while que:
            n = len(que)
            maxx = float('-inf')
            for i in range(n):
                node = que.popleft()
                if node.val > maxx:
                    maxx = node.val
                if node.left:
                    que.append(node.left)
                if node.right:
                    que.append(node.right)
            res.append(maxx)
        return res
        